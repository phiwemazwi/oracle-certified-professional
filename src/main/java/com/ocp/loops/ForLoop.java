package com.ocp.loops;

public class ForLoop {

    public static void main(String[] args){


        /*Question 1
        * 1. Initialize i to 15
        * 2. While i < 25
        * 3. Print i
        * 4. Increase i by 1

        for (int i = 15; i < 25; i++ ){
            System.out.println(i);
        }

         */









        /* Question 2
        * 1. Initialise i to 10
        * 2. While i >= 1
        * 3. Print i
        * 4  Subtract i by 1


        for (int i = 10; i >= 1; i--){
            System.out.println(i);
        }
         */

        /* Question 3
        * 1. Initialise i to 1
        * 2. While i is <=  15
        * 3. Print i
        * 4. Increase i by 2: i = i + 2 (i += 2)

        for (int i = 1; i <= 15; i += 2){
            System.out.println(i);
        }
         */

        /*Question 4
        * 1. Initialise i to 0
        * 2. While i is <= 12
        * 3. Print i
        * 4. Increase i by 1

        for (int i = 0; i <= 12; i++){
            System.out.println(i);
        }

         */

        /*Question 5
        * 1. Start i at 40
        * 2. While i >= 20
        * 2. Decrease i by 5 (i = i - 5)
        * 3. Print i
         */

        for (int i = 40; i >= 20; i -= 5){
            System.out.println(i);
        }





    }


}
